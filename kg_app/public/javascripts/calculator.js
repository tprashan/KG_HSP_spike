var calculator = {};
exports.calculator = calculator;

calculator.add = function(digit_1,digit_2){
  return digit_1 + digit_2;
}


calculator.subtract = function(digit_1,digit_2){
  return digit_1 - digit_2;
}

calculator.multiply = function(digit_1,digit_2){
  return digit_1 * digit_2;
}

calculator.divide = function(digit_1,digit_2){
  return digit_1 / digit_2;
}
